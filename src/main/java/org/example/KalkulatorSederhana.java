package org.example;

public class KalkulatorSederhana {
    public static int tambah(int angka1, int angka2) {
        return angka1 + angka2;
    }


    public static int kurang(int angka1, int angka2) {
        return angka1 - angka2;
    }


    public static int kali(int angka1, int angka2) {
        return angka1 * angka2;
    }


    public static int bagi(int angka1, int angka2) {
        return angka1 / angka2;
    }


    public static int sisaBagi(int angka1, int angka2) {
        return angka1 % angka2;
    }


    public static double tambahDesimal(double angka1, double angka2) {
        return angka1 + angka2;
    }


    public static double kurangDesimal(double angka1, double angka2) {
        return angka1 - angka2;
    }


    public static double kaliDesimal(double angka1, double angka2) {
        return angka1 * angka2;
    }


    public static double bagiDesimal(double angka1, double angka2) {
        return angka1 / angka2;
    }


    public static float tambahFloat(float angka1, float angka2) {
        return angka1 + angka2;
    }


    public static float kurangFloat(float angka1, float angka2) {
        return angka1 - angka2;
    }


    public static float kaliFloat(float angka1, float angka2) {
        return angka1 * angka2;
    }


    public static float bagiFloat(float angka1, float angka2) {
        return angka1 / angka2;
    }

}
